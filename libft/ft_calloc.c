#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	ptr = malloc(size * count);
	if (!ptr)
	{
		return (0);
	}
	ft_memset(ptr, 0, (count * size));
	return (ptr);
}
